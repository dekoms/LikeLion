class Student:
    def __init__(self, id, name, age, major, grade):
        self.__id  = id
        self.__name = name
        self.__age = age
        self.__major = major
        self.__grade = grade
    def __create__(self):
        self.__id  = id
    def __read__(self):
        self.__id  = id
    def __update__(self):
        self.__id  = id
    def __delete__(self):
        self.__id  = id
